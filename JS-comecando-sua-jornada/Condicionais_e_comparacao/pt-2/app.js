// Condicionais e comparação

// comparando textos
var nome = "Matheus";


if (nome === "Matheus") {
    console.log("Bem-vindo, Matheus");
}

// comparação com booleano
var userOnline = false;

if (userOnline === true) {
    console.log("O usuário está online!");
} else {
    console.log("O usuário está offline!");
}

// usando operador ternário
var numero = 90;

numero === 10 ? console.log("O número é igual a 10") : console.log("O número é diferente de 10");