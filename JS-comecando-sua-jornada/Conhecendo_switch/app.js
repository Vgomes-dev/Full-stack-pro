// Aprendendo Loops

// 1° while = enquanto a condição for verdadeira, o código dentro do while será executado.

var x = 5;

while(x < 10){
    document.write("<h3 style='color: red;'> <br> o valor do X é: " + x + " </h3>");

    // aumentar o valor de X. ex: X = X + 1
    x++;
}

// FOR = PARA 

for(a = 0; a <= 10; a++){
    document.write("<h3 style='color: blue;'> <br> o valor do A é: " + a + " </h3>");
}